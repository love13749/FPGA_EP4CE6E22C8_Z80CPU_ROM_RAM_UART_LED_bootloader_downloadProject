; ============================================================
; project.asm
; Z80 测试程序 - 通过串口输出信息并控制LED
; 
; 功能: 上电后通过串口输出信息，LED闪烁
; 编译: ORG $2000 (下载到 RAM 运行)
; ============================================================

; ============================================================
; 系统常量定义
; ============================================================
UART_DATA   EQU $81      ; UART 数据端口
UART_STAT   EQU $80      ; UART 状态端口 (bit0=可读, bit1=可写)
LED_PORT    EQU $90      ; LED 输出端口

; ============================================================
; 程序入口 - 必须从 $2000 开始
; ============================================================
    ORG $2000

START:
    ; --- 初始化栈指针 ---
    LD SP, $2FFF
    
    ; --- 输出启动信息 ---
    LD HL, MSG_START
    CALL PRINT_STRING
    
    ; --- 主循环：LED流水灯 + 串口输出 ---
    LD A, $01            ; 初始 LED 值 (bit0亮)
    
MAIN_LOOP:
    ; 输出LED
    CPL                  ; A值每位取反
    OUT (LED_PORT), A
    CPL                  ; 恢复A值

    ; 发送当前LED状态到串口
    PUSH AF
    CALL SEND_HEX
    LD A, ' '
    CALL UART_PUTCHAR
    POP AF
    
    ; 延时
    CALL DELAY
    
    ; 左移一位（流水灯效果）
    RLCA
    CP $00               ; 如果移出所有位
    JR NZ, MAIN_LOOP
    LD A, $01            ; 重新从 bit0 开始
    JR MAIN_LOOP

; ============================================================
; 发送一个字节的十六进制值到串口
; 输入: A = 要发送的字节
; ============================================================
SEND_HEX:
    PUSH AF
    PUSH AF
    ; 高4位
    RRA
    RRA
    RRA
    RRA
    CALL HEX_TO_ASCII
    CALL UART_PUTCHAR
    ; 低4位
    POP AF
    CALL HEX_TO_ASCII
    CALL UART_PUTCHAR
    POP AF
    RET

; ============================================================
; 4位二进制转ASCII十六进制
; 输入: A = 低4位为要转换的值
; 输出: A = ASCII字符 ('0'-'9' 或 'A'-'F')
; ============================================================
HEX_TO_ASCII:
    AND $0F
    CP $0A
    JR C, _HTA_DIGIT
    ADD A, 'A' - 10
    RET
_HTA_DIGIT:
    ADD A, '0'
    RET

; ============================================================
; UART 发送一个字符
; 输入: A = 要发送的字符
; ============================================================
UART_PUTCHAR:
    PUSH AF
_UART_PUT_WAIT:
    IN A, (UART_STAT)
    BIT 1, A
    JR Z, _UART_PUT_WAIT
    POP AF
    OUT (UART_DATA), A
    RET

; ============================================================
; 输出字符串
; 输入: HL = 字符串首地址 (以 0 结尾)
; ============================================================
PRINT_STRING:
    PUSH AF
    PUSH HL
_PS_LOOP:
    LD A, (HL)
    CP 0
    JR Z, _PS_END
    CALL UART_PUTCHAR
    INC HL
    JR _PS_LOOP
_PS_END:
    POP HL
    POP AF
    RET
    

; ============================================================
; 延时子程序
; ============================================================
DELAY:
    PUSH BC
    LD B, $FF
_DELAY_OUTER:
    LD C, $FF
_DELAY_INNER:
    DEC C
    JR NZ, _DELAY_INNER
    DEC B
    JR NZ, _DELAY_OUTER
    POP BC
    RET

; ============================================================
; 字符串常量
; ============================================================
MSG_START:
    DB 0Dh, 0Ah
    DB "Z80 Program Started!", 0Dh, 0Ah
    DB "LED Running...", 0Dh, 0Ah
    DB 0

; ============================================================
; 程序结束
; ============================================================
    END START